def c_string(value: object) -> str:
    """Python 값을 C 문자열 리터럴로 안전하게 이스케이프합니다."""
    text = str(value)
    return '"' + text.replace("\\", "\\\\").replace('"', '\\"') + '"'


def c_float(value: object) -> str:
    """Python float을 STM32 C 헤더에 넣을 수 있는 float 리터럴로 변환합니다."""
    number = float(value)
    if not np.isfinite(number):
        raise ValueError(f"cannot export non-finite float: {number}")
    text = f"{number:.9g}"
    if "e" not in text.lower() and "." not in text:
        text += ".0"
    return f"{text}f"


def chunked(values: list[str], per_line: int) -> str:
    """C 배열 초기화 값을 줄 단위로 나눠 사람이 읽기 좋게 만듭니다."""
    return ",\n".join(
        "    " + ", ".join(values[start : start + per_line])
        for start in range(0, len(values), per_line)
    )


def c_string_array(name: str, values: list[object]) -> str:
    """라벨명/feature명 같은 문자열 목록을 C static 배열 선언으로 만듭니다."""
    return f"static const char *const {name}[{len(values)}] = {{\n{chunked([c_string(v) for v in values], 4)}\n}};\n"


def c_float_array(name: str, values: np.ndarray, per_line: int = 6) -> str:
    """1차원 float 배열을 C static const float 초기화 코드로 만듭니다."""
    flat = np.asarray(values, dtype=np.float32).reshape(-1)
    return f"static const float {name}[{flat.size}] = {{\n{chunked([c_float(v) for v in flat], per_line)}\n}};\n"


def c_float_matrix(name: str, values: np.ndarray, per_line: int = 6) -> str:
    """2차원 float 행렬을 C static const float 초기화 코드로 만듭니다."""
    array = np.asarray(values, dtype=np.float32)
    rows = []
    for row in array:
        rows.append("  {\n" + chunked([c_float(v) for v in row], per_line) + "\n  }")
    return f"static const float {name}[{array.shape[0]}][{array.shape[1]}] = {{\n" + ",\n".join(rows) + "\n};\n"


def c_u8_array(name: str, values: np.ndarray, per_line: int = 12) -> str:
    """정수 인덱스 배열을 C static const uint8_t 초기화 코드로 만듭니다."""
    flat = np.asarray(values, dtype=np.uint8).reshape(-1)
    return f"static const uint8_t {name}[{flat.size}] = {{\n{chunked([str(int(v)) for v in flat], per_line)}\n}};\n"


def header_preamble(guard: str, description: str) -> str:
    """자동 생성 C 헤더의 include guard와 공통 include를 시작합니다."""
    return "\n".join(
        [
            "/* Auto-generated by full56_pipeline/scripts/install_full56_firmware_candidate.py. */",
            f"/* {description} */",
            f"#ifndef {guard}",
            f"#define {guard}",
            "",
            "#include <stdint.h>",
            "",
        ]
    )


def header_end(guard: str) -> str:
    """자동 생성 C 헤더의 include guard를 닫습니다."""
    return f"\n#endif /* {guard} */\n"


def load_keras_model(tf):
    """설치 기준 Keras full56 모델이 존재하는지 확인하고 로드합니다."""
    if not FULL56_MODEL.exists():
        raise FileNotFoundError(f"missing Keras model: {FULL56_MODEL}")
    return tf.keras.models.load_model(FULL56_MODEL)


def export_tflite(tf, model) -> dict:
    """Keras 모델을 설치 기준 TFLite 파일로 다시 변환하고 크기를 기록합니다."""
    converter = tf.lite.TFLiteConverter.from_keras_model(model)
    FULL56_TFLITE.parent.mkdir(parents=True, exist_ok=True)
    FULL56_TFLITE.write_bytes(converter.convert())
    return {"path": str(FULL56_TFLITE), "size_bytes": int(FULL56_TFLITE.stat().st_size)}


def tflite_predict(tf, features: np.ndarray) -> np.ndarray:
    """설치 기준 TFLite 모델을 여러 feature 행에 대해 실행해 확률 행렬을 반환합니다."""
    interpreter = tf.lite.Interpreter(model_content=FULL56_TFLITE.read_bytes())
    interpreter.allocate_tensors()
    input_detail = interpreter.get_input_details()[0]
    output_detail = interpreter.get_output_details()[0]
    outputs = []
    for row in np.asarray(features, dtype=np.float32):
        interpreter.set_tensor(input_detail["index"], row.reshape(input_detail["shape"]).astype(input_detail["dtype"]))
        interpreter.invoke()
        outputs.append(interpreter.get_tensor(output_detail["index"]).reshape(-1).astype(np.float32))
    return np.asarray(outputs, dtype=np.float32)


def evaluate_tflite(tf, feature_columns: list[str], labels: list[str]) -> dict:
    """설치 직전 TFLite가 테스트 CSV에서 기대 정확도를 유지하는지 재검증합니다."""
    df = pd.read_csv(FULL56_FEATURES)
    test_df = df[df["split"].astype(str) == "test"].copy()
    x_test = test_df[feature_columns].replace([np.inf, -np.inf], np.nan).fillna(0.0).to_numpy(dtype=np.float32)
    probs = tflite_predict(tf, x_test)
    pred_indices = np.argmax(probs, axis=1)
    pred_labels = [labels[int(i)] for i in pred_indices]
    accuracy = float(np.mean(test_df["label"].astype(str).to_numpy() == np.asarray(pred_labels, dtype=object)))

    pred_out = FULL56_CANDIDATE_DIR / "reports" / "full56_tflite_frame_predictions.csv"
    pred_out.parent.mkdir(parents=True, exist_ok=True)
    out_df = test_df[["file", "path", "label", "source_label", "breath_active"]].copy()
    out_df["pred_label"] = pred_labels
    out_df["pred_prob"] = np.max(probs, axis=1)
    out_df.to_csv(pred_out, index=False, encoding="utf-8")
    return {"frame_accuracy": accuracy, "predictions_csv": str(pred_out)}


def normalization_stats(model, feature_count: int) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Keras Normalization 레이어의 mean/variance/inv_std를 펌웨어 헤더용 배열로 추출합니다."""
    layer = model.get_layer("feature_normalization")
    weights = layer.get_weights()
    if len(weights) < 2:
        raise RuntimeError("feature_normalization layer has no mean/variance weights")
    mean = np.asarray(weights[0], dtype=np.float32).reshape(-1)
    variance = np.asarray(weights[1], dtype=np.float32).reshape(-1)
    if mean.size != feature_count or variance.size != feature_count:
        raise ValueError(f"normalization size mismatch: {mean.size}/{variance.size} vs {feature_count}")
    inv_std = 1.0 / np.sqrt(variance + 1.0e-7)
    return mean, variance, inv_std.astype(np.float32)


def run_stedgeai(network_name: str) -> dict:
    """production 네트워크명으로 ST Edge AI C 코드를 재생성하고 manifest를 저장합니다."""
    tool = stedgeai_utils.resolve_stedgeai_tool(None)
    run_id = timestamp()
    run_dir = FULL56_CANDIDATE_DIR / "stedgeai_runs" / f"run_{run_id}_{network_name}"
    output_dir = run_dir / "stedgeai_output"
    workspace_dir = run_dir / "workspace"
    output_dir.mkdir(parents=True, exist_ok=True)
    workspace_dir.mkdir(parents=True, exist_ok=True)
    temp_root = Path(tempfile.gettempdir()) / "stm32_breath_full56_install" / f"run_{run_id}_{network_name}"
    temp_model_dir = temp_root / "model"
    temp_output_dir = temp_root / "output"
    temp_workspace_dir = temp_root / "workspace"
    temp_model_dir.mkdir(parents=True, exist_ok=True)
    temp_output_dir.mkdir(parents=True, exist_ok=True)
    temp_workspace_dir.mkdir(parents=True, exist_ok=True)
    staged_model = temp_model_dir / FULL56_TFLITE.name
    shutil.copy2(FULL56_TFLITE, staged_model)
    command = [
        tool,
        "generate",
        "-m",
        str(staged_model),
        "--target",
        "stm32f4",
        "--type",
        "tflite",
        "--name",
        network_name,
        "--output",
        str(temp_output_dir),
        "--workspace",
        str(temp_workspace_dir),
        "--verbosity",
        "1",
    ]
    completed = subprocess.run(
        command,
        check=False,
        capture_output=True,
        text=True,
        encoding="utf-8",
        errors="replace",
    )
    (run_dir / "stedgeai_stdout.txt").write_text(completed.stdout, encoding="utf-8")
    (run_dir / "stedgeai_stderr.txt").write_text(completed.stderr, encoding="utf-8")
    if temp_output_dir.exists():
        shutil.copytree(temp_output_dir, output_dir, dirs_exist_ok=True)
    manifest = {
        "status": "ok" if completed.returncode == 0 else "failed",
        "returncode": int(completed.returncode),
        "run_id": run_id,
        "run_dir": str(run_dir),
        "stedgeai_output_dir": str(output_dir),
        "network_name": network_name,
        "command_text": subprocess.list2cmdline(command),
        "stdout_tail": completed.stdout[-4000:],
        "stderr_tail": completed.stderr[-4000:],
        "generated_files": sorted(p.name for p in output_dir.glob("*") if p.is_file()),
    }
    write_json(run_dir / "stedgeai_full56_install_summary.json", manifest)
    write_json(FULL56_CANDIDATE_DIR / "latest_stedgeai_breath_mlp_summary.json", manifest)
    if completed.returncode != 0:
        raise RuntimeError(manifest["stderr_tail"] or manifest["stdout_tail"])
    return manifest


