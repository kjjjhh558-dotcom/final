def main(argv: list[str] | None = None) -> int:
    """full56 후보 검증, 백업, C 코드/헤더 설치, 요약 저장을 한 번에 수행합니다."""
    parser = argparse.ArgumentParser()
    parser.add_argument("--network-name", default="breath_mlp")
    parser.add_argument("--no-backup", action="store_true")
    args = parser.parse_args(argv)
    configure_stdio()

    import tensorflow as tf

    metrics = read_json(FULL56_METRICS)
    model = load_keras_model(tf)
    tflite_info = export_tflite(tf, model)
    tflite_eval = evaluate_tflite(tf, list(metrics["feature_columns"]), list(metrics["labels"]))
    stedgeai = run_stedgeai(args.network_name)

    backup_dir = PROJECT_ROOT / "backups" / "firmware_assets" / "not_created"
    if not args.no_backup:
        backup_dir = backup_current_firmware_assets()

    install_generated_files(Path(stedgeai["stedgeai_output_dir"]))
    write_config_header(FW_ASSETS_DIR / "breath_ai_config.h", metrics, tflite_info, tflite_eval, model)
    write_dsp_header(FW_ASSETS_DIR / "breath_dsp_tables.h")
    write_golden_header(FW_ASSETS_DIR / "breath_golden_vectors.h", metrics, tf)
    write_assets_readme(FW_ASSETS_DIR / "README.md", metrics)
    write_model_selection(FW_ASSETS_DIR / "model_selection.json", metrics, stedgeai, tflite_info, tflite_eval, backup_dir)

    summary = {
        "status": "ok",
        "created_at": datetime.now().isoformat(timespec="seconds"),
        "firmware_generated_dir": str(FW_GENERATED_DIR),
        "firmware_assets_dir": str(FW_ASSETS_DIR),
        "fallback_30feature_dir": str(backup_dir),
        "feature_count": 56,
        "frame_size": FRAME_SIZE,
        "hop_size": HOP_SIZE,
        "fft_size": FFT_SIZE,
        "delta_lookahead_ms": DELTA_LOOKAHEAD_MS,
        "tflite": tflite_info,
        "tflite_eval": tflite_eval,
        "stedgeai": stedgeai,
    }
    write_json(FULL56_CANDIDATE_DIR / "installed_to_mouthnose_summary.json", summary)

    print("=== Full56 Firmware Candidate Installed ===")
    print(f"generated : {FW_GENERATED_DIR}")
    print(f"assets    : {FW_ASSETS_DIR}")
    print(f"fallback  : {backup_dir}")
    print(f"tflite acc: {tflite_eval['frame_accuracy'] * 100.0:.2f}%")
    print(f"delay     : {DELTA_LOOKAHEAD_MS} ms lookahead for exact delta2")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
