def write_dsp_header(path: Path) -> None:
    """대역통과 SOS, Hann window, mel filterbank를 breath_dsp_tables.h로 작성합니다."""
    sos = ef.design_bandpass_filter(SAMPLE_RATE, BPF_LOW_HZ, BPF_HIGH_HZ, BPF_ORDER).astype(np.float32)
    window = signal.get_window(WINDOW_TYPE, FRAME_SIZE, fftbins=True).astype(np.float32)
    mel = ef.build_mel_filterbank(SAMPLE_RATE, FFT_SIZE, NUM_MELS, MFCC_FMIN_HZ, MFCC_FMAX_HZ).astype(np.float32)
    lines = [
        header_preamble("BREATH_DSP_TABLES_H", "Full56 causal DSP tables."),
        f"#define BREATH_DSP_SOS_SECTIONS {sos.shape[0]}",
        f"#define BREATH_DSP_SOS_COEFFS {sos.shape[1]}",
        f"#define BREATH_DSP_WINDOW_SIZE {window.shape[0]}",
        f"#define BREATH_DSP_NUM_MELS {mel.shape[0]}",
        f"#define BREATH_DSP_FFT_BINS {mel.shape[1]}",
        "",
        c_float_matrix("BREATH_DSP_FILTER_SOS", sos),
        c_float_array("BREATH_DSP_HANN_WINDOW", window),
        c_float_matrix("BREATH_DSP_MEL_FILTERBANK", mel),
        header_end("BREATH_DSP_TABLES_H"),
    ]
    path.write_text("\n".join(lines), encoding="utf-8", newline="\n")


def select_golden_rows(df: pd.DataFrame, labels: list[str], count_per_label: int = 2) -> pd.DataFrame:
    """STM32 self-test에 넣을 대표 테스트 feature 행을 라벨별로 선택합니다."""
    test_df = df[df["split"].astype(str) == "test"].copy()
    rows = []
    for label in labels:
        group = test_df[test_df["label"].astype(str) == label]
        if group.empty:
            group = df[df["label"].astype(str) == label]
        rows.append(group.head(count_per_label))
    return pd.concat(rows, ignore_index=True)


def write_golden_header(path: Path, metrics: dict, tf) -> None:
    """골든 feature와 TFLite 확률을 C 헤더로 저장해 펌웨어 self-test 기준값으로 씁니다."""
    labels = list(metrics["labels"])
    feature_columns = list(metrics["feature_columns"])
    df = pd.read_csv(FULL56_FEATURES)
    golden_df = select_golden_rows(df, labels, count_per_label=2)
    features = golden_df[feature_columns].replace([np.inf, -np.inf], np.nan).fillna(0.0).to_numpy(dtype=np.float32)
    probs = tflite_predict(tf, features)
    label_to_index = {label: index for index, label in enumerate(labels)}
    pred_indices = np.argmax(probs, axis=1).astype(np.uint8)

    lines = [
        header_preamble("BREATH_GOLDEN_VECTORS_H", "Full56 model-input golden vectors for STM32 self-test."),
        '#include "breath_ai_config.h"',
        "",
        f"#define BREATH_GOLDEN_VECTOR_COUNT {features.shape[0]}",
        f"#define BREATH_GOLDEN_FRAME_SIZE {FRAME_SIZE}",
        f"#define BREATH_GOLDEN_FEATURE_COUNT {features.shape[1]}",
        f"#define BREATH_GOLDEN_MODEL_OUTPUT_COUNT {len(labels)}",
        "#define BREATH_GOLDEN_HAS_MODEL_OUTPUTS 1",
        "",
        c_string_array("BREATH_GOLDEN_VECTOR_IDS", [f"full56_gv_{i:03d}" for i in range(features.shape[0])]),
        c_string_array("BREATH_GOLDEN_LABELS", golden_df["label"].astype(str).tolist()),
        c_string_array("BREATH_GOLDEN_SPLITS", golden_df["split"].astype(str).tolist()),
        c_string_array("BREATH_GOLDEN_FILES", golden_df["file"].astype(str).tolist()),
        c_u8_array("BREATH_GOLDEN_LABEL_INDEX", np.asarray([label_to_index[str(v)] for v in golden_df["label"]], dtype=np.uint8)),
        c_float_matrix("BREATH_GOLDEN_FEATURES", features),
        c_float_matrix("BREATH_GOLDEN_MLP_PROBS", probs),
        c_u8_array("BREATH_GOLDEN_MLP_PRED_LABEL_INDEX", pred_indices),
        c_float_array("BREATH_GOLDEN_MLP_PRED_PROB", np.max(probs, axis=1).astype(np.float32)),
        header_end("BREATH_GOLDEN_VECTORS_H"),
    ]
    path.write_text("\n".join(lines), encoding="utf-8", newline="\n")


